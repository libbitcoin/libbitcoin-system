// Digest 320 changes IV (specialize template).
template <size_t Digest = 160,
    bool_if<Digest == 160 || Digest == 320> = true>
struct h160
  : public h<k160, Digest>
{
    using state_t = typename h<k160, Digest>::state_t;

    // initial value, h()
    static constexpr state_t get
    {
        0x67452301,
        0xefcdab89,
        0x98badcfe,
        0x10325476,
        0xc3d2e1f0
    };
};

